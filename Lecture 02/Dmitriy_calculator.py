number_a = int(input("Введите первое число: "))
number_b = int(input("ВВедите второе число:"))

print('\nРезультат: + - * /')
print(f'{number_a} + {number_b} = {number_a + number_b}')
print(f'{number_a} - {number_b} = {number_a - number_b}')
print(f'{number_a} * {number_b} = {number_a * number_b}')
print(f'{number_a} / {number_b} = {number_a / number_b}')